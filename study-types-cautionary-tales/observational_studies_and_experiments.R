# TYPES OF STUDIES
# Observational study:
## collect data in a way that does not directly interfere with how the data arise
## only correlation can be inferred
## typically uses random sampling, but not random assignment
## the results are generalizable

# Experiment:
## randomly assign subjects to various treatments
## causation can be inferred
## typically uses random assignment, but not random sampling
## the studies can be used to make causal conclusion, but it's not generalizable

# RANDOM SAMPLING
## At selection of subjects from population
## helps generalizability of results

# RANDOM ASSIGNMENT
## At selection of subjects from population
## helps infer causation from results


